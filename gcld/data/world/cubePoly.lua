function cityPath.curve(cityID1, cityID2)
    local funName = "curve_" .. tostring(cityID1) .. "_" .. tostring(cityID2)
     if cityID1 > cityID2 then
        funName = "curve_" .. tostring(cityID2) .. "_" .. tostring(cityID1)
     end
    if cityPath[funName] == nil then
        return nil
    end
    return cityPath[funName]();
end

function cityPath.position(cityID1, cityID2, percent)
    if cityID1 > cityID2 then
        percent = 1.0 - percent;
    end

    x = cityPath.data.x[0];
    y = cityPath.data.y[0];

    for i =1, cityPath.data.rank do
        x = x*percent + cityPath.data.x[i];
        y = y*percent + cityPath.data.y[i];
    end

    return x,y;
end
